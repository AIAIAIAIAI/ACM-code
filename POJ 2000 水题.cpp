#include<iostream>
using namespace std;
const int maxn=1e5+5;
int a[maxn];
int main()
{
	int n,cur=1,cnt=1;
	while(true)
	{
		for(int i=1;i<=cur;i++)
		{
			a[cnt]=a[cnt-1]+cur;
			cnt++;
		}
		cur++;
		if(cnt>10000)
			break;
	}
	while(cin>>n,n)
	{
		cout<<n<<" "<<a[n]<<endl;
	}
	return 0;
}